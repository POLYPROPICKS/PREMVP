-- ============================================================================
-- track_record_window_results — clean UI read-model for the trust-block
-- track record. Founder-approved new table (see PR track-record-window-results).
--
-- DATA FLOW (see docs/ai-context/REAL_RESOLVED_TRACK_RECORD_FLOW.md):
--   1. public.track_record_display_signals  = SELECTED 7D/14D signal universe
--      (product rules decide WHICH signals belong to a window).
--   2. public.generated_signal_pairs        = REAL resolver outcome source
--      (written by signal-resolve-cron: signal_result, resolved_at,
--       winning_outcome, entry_price_num, realized_return_pct).
--   3. public.track_record_window_results    = materialized UI read-model:
--      selected universe + joined real outcomes + computed real PnL.
--
-- FORBIDDEN: projected_return_usd / projected_pnl_units /
--            projected_win_probability MUST NOT be used for real PnL here.
-- ============================================================================

BEGIN;

-- ── Table ───────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.track_record_window_results (
  id                        bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  window_days               integer NOT NULL CHECK (window_days IN (7, 14)),
  source_row_id             uuid NOT NULL,
  score_rank                integer,
  match_key                 text,
  signal_key                text,
  event_title               text NOT NULL,
  market_question           text,
  selected_outcome          text,
  position                  text,
  source_model              text,
  selection_generated_at    timestamptz,
  selection_latest_batch_at timestamptz,
  result_source_table       text NOT NULL DEFAULT 'generated_signal_pairs',
  signal_result             text,
  display_status            text NOT NULL CHECK (display_status IN ('Hit', 'Miss', 'Pending')),
  is_resolved               boolean NOT NULL DEFAULT false,
  resolved_at               timestamptz,
  winning_outcome           text,
  entry_price_num           numeric,
  decimal_odds              numeric,
  stake_usd                 numeric NOT NULL DEFAULT 100,
  real_pnl_usd              numeric,
  return_label              text NOT NULL DEFAULT '—',
  metric_formula_version    text NOT NULL DEFAULT 'realized-flat-stake-v1',
  generated_at              timestamptz NOT NULL DEFAULT now(),
  row_hash                  text
);

-- ── Constraints / indexes ───────────────────────────────────────────────────
CREATE UNIQUE INDEX IF NOT EXISTS track_record_window_results_window_source_uidx
  ON public.track_record_window_results (window_days, source_row_id);
CREATE INDEX IF NOT EXISTS track_record_window_results_window_rank_idx
  ON public.track_record_window_results (window_days, score_rank);
CREATE INDEX IF NOT EXISTS track_record_window_results_window_resolved_idx
  ON public.track_record_window_results (window_days, is_resolved, resolved_at DESC);
CREATE INDEX IF NOT EXISTS track_record_window_results_source_row_idx
  ON public.track_record_window_results (source_row_id);
CREATE INDEX IF NOT EXISTS track_record_window_results_generated_at_idx
  ON public.track_record_window_results (generated_at DESC);

-- ── Comments ────────────────────────────────────────────────────────────────
COMMENT ON TABLE public.track_record_window_results IS
  'Clean UI read-model for the trust-block track record. Selected universe comes '
  'from track_record_display_signals; real results come from generated_signal_pairs. '
  'projected_* EV fields are FORBIDDEN for real PnL here.';
COMMENT ON COLUMN public.track_record_window_results.source_row_id IS
  'track_record_display_signals.source_row_id (== generated_signal_pairs.id).';
COMMENT ON COLUMN public.track_record_window_results.real_pnl_usd IS
  'Realized flat-$100-stake PnL from generated_signal_pairs only. Never projected EV.';
COMMENT ON COLUMN public.track_record_window_results.display_status IS
  'Hit=won, Miss=lost, Pending=unresolved. Derived from real signal_result, never projected EV.';

-- ── RLS: no broad public write; API reads server-side via service role. ──────
ALTER TABLE public.track_record_window_results ENABLE ROW LEVEL SECURITY;

COMMIT;

-- ============================================================================
-- REFRESH: repopulate the read-model from the selected universe joined to real
-- outcomes. Idempotent UPSERT on (window_days, source_row_id); stale rows that
-- no longer exist in track_record_display_signals for window 7/14 are deleted.
--
-- Join: track_record_display_signals.source_row_id -> generated_signal_pairs.id
--       (g.id::text = d.source_row_id)
-- ============================================================================

BEGIN;

WITH selected AS (
  SELECT
    d.window_days::int                                          AS window_days,
    d.source_row_id::uuid                                       AS source_row_id,
    d.score_rank                                                AS score_rank,
    d.match_key                                                 AS match_key,
    d.signal_key                                                AS signal_key,
    d.event_title                                               AS event_title,
    d.market_question                                           AS market_question,
    COALESCE(d.selected_outcome, d.position)                    AS selected_outcome,
    d.position                                                  AS position,
    d.source_model                                              AS source_model,
    d.generated_at                                              AS selection_generated_at,
    d.batch_day                                                 AS selection_latest_batch_at,
    COALESCE(d.stake_usd, 100)::numeric                         AS stake_usd,
    d.decimal_odds                                              AS d_decimal_odds,
    g.signal_result                                             AS g_signal_result_raw,
    g.resolved_at                                               AS g_resolved_at,
    g.winning_outcome                                           AS g_winning_outcome,
    g.entry_price_num                                           AS g_entry_price_num
  FROM public.track_record_display_signals d
  LEFT JOIN public.generated_signal_pairs g
    ON g.id::text = d.source_row_id
  WHERE d.window_days IN (7, 14)
),
computed AS (
  SELECT
    s.*,
    lower(s.g_signal_result_raw) AS signal_result,
    (s.g_resolved_at IS NOT NULL
      AND lower(s.g_signal_result_raw) IN ('won', 'lost')) AS is_resolved,
    CASE
      WHEN lower(s.g_signal_result_raw) = 'won'  THEN 'Hit'
      WHEN lower(s.g_signal_result_raw) = 'lost' THEN 'Miss'
      ELSE 'Pending'
    END AS display_status,
    CASE
      WHEN s.g_entry_price_num > 0 AND s.g_entry_price_num < 1
        THEN 1.0 / s.g_entry_price_num
      WHEN s.d_decimal_odds > 1
        THEN s.d_decimal_odds
      ELSE NULL
    END AS decimal_odds,
    CASE
      WHEN lower(s.g_signal_result_raw) = 'won'
        AND s.g_entry_price_num > 0 AND s.g_entry_price_num < 1
        THEN s.stake_usd * ((1.0 / s.g_entry_price_num) - 1)
      WHEN lower(s.g_signal_result_raw) = 'lost'
        THEN -s.stake_usd
      ELSE NULL
    END AS real_pnl_usd
  FROM selected s
)
INSERT INTO public.track_record_window_results (
  window_days, source_row_id, score_rank, match_key, signal_key,
  event_title, market_question, selected_outcome, position, source_model,
  selection_generated_at, selection_latest_batch_at, result_source_table,
  signal_result, display_status, is_resolved, resolved_at, winning_outcome,
  entry_price_num, decimal_odds, stake_usd, real_pnl_usd, return_label,
  metric_formula_version, generated_at, row_hash
)
SELECT
  c.window_days,
  c.source_row_id,
  c.score_rank,
  c.match_key,
  c.signal_key,
  COALESCE(c.event_title, 'Unknown market'),
  c.market_question,
  c.selected_outcome,
  c.position,
  c.source_model,
  c.selection_generated_at,
  c.selection_latest_batch_at,
  'generated_signal_pairs',
  c.signal_result,
  c.display_status,
  c.is_resolved,
  c.g_resolved_at,
  c.g_winning_outcome,
  c.g_entry_price_num,
  c.decimal_odds,
  c.stake_usd,
  c.real_pnl_usd,
  CASE
    WHEN c.display_status = 'Hit'  THEN '+$' || round(c.real_pnl_usd)::text
    WHEN c.display_status = 'Miss' THEN '-$' || round(abs(c.real_pnl_usd))::text
    ELSE '—'
  END,
  'realized-flat-stake-v1',
  now(),
  md5(
    c.window_days::text || '|' || c.source_row_id::text || '|' ||
    COALESCE(c.signal_result, '') || '|' || COALESCE(c.real_pnl_usd::text, '')
  )
FROM computed c
ON CONFLICT (window_days, source_row_id) DO UPDATE SET
  score_rank                = EXCLUDED.score_rank,
  match_key                 = EXCLUDED.match_key,
  signal_key                = EXCLUDED.signal_key,
  event_title               = EXCLUDED.event_title,
  market_question           = EXCLUDED.market_question,
  selected_outcome          = EXCLUDED.selected_outcome,
  position                  = EXCLUDED.position,
  source_model              = EXCLUDED.source_model,
  selection_generated_at    = EXCLUDED.selection_generated_at,
  selection_latest_batch_at = EXCLUDED.selection_latest_batch_at,
  signal_result             = EXCLUDED.signal_result,
  display_status            = EXCLUDED.display_status,
  is_resolved               = EXCLUDED.is_resolved,
  resolved_at               = EXCLUDED.resolved_at,
  winning_outcome           = EXCLUDED.winning_outcome,
  entry_price_num           = EXCLUDED.entry_price_num,
  decimal_odds              = EXCLUDED.decimal_odds,
  stake_usd                 = EXCLUDED.stake_usd,
  real_pnl_usd              = EXCLUDED.real_pnl_usd,
  return_label              = EXCLUDED.return_label,
  generated_at              = now(),
  row_hash                  = EXCLUDED.row_hash;

-- Delete stale rows no longer present in the selected universe for window 7/14.
DELETE FROM public.track_record_window_results w
WHERE w.window_days IN (7, 14)
  AND NOT EXISTS (
    SELECT 1
    FROM public.track_record_display_signals d
    WHERE d.window_days = w.window_days
      AND d.source_row_id::uuid = w.source_row_id
  );

COMMIT;
